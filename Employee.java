public class Employee {
    private String fName, lName, adress, email, socialSecurityNr;
    private int firstWorkYear;

    public Employee(String fName, String lName, String adress, String email, String socialSecurityNr, int firstWorkYear) {
        this.fName = fName;
        this.lName = lName;
        this.adress = adress;
        this.email = email;
        this.socialSecurityNr = socialSecurityNr;
        this.firstWorkYear = firstWorkYear;
    }

}
